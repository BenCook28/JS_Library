// Redux is a React library.

// Redux solves the problem of mutable
// state, which means any part of the
// application can change a state.  With 
// Redux, only actions change the state
// of the immutable store.

// It also reduces code duplication through 
// action creators.

// It also makes code easier to mantain.
// Subscriptions update the state whenever
// something changes.  Reducers allow 
// state manipulation to happen in one place.

